package com.gea.contactapp;

public class ContactModel {
    String ContactId, ContactImage, ContactName, ContactEmail, ContactPhone, ContactDateBirth, ContactGender, ContactNote;


    public String getContactId() {
        return ContactId;
    }

    public void setContactId(String contactId) {
        ContactId = contactId;
    }

    public String getContactImage() {
        return ContactImage;
    }

    public void setContactImage(String contactImage) {
        ContactImage = contactImage;
    }

    public String getContactName() {
        return ContactName;
    }

    public void setContactName(String contactName) {
        ContactName = contactName;
    }

    public String getContactEmail() {
        return ContactEmail;
    }

    public void setContactEmail(String contactEmail) {
        ContactEmail = contactEmail;
    }

    public String getContactPhone() {
        return ContactPhone;
    }

    public void setContactPhone(String contactPhone) {
        ContactPhone = contactPhone;
    }

    public String getContactDateBirth() {
        return ContactDateBirth;
    }

    public void setContactDateBirth(String contactDateBirth) {
        ContactDateBirth = contactDateBirth;
    }

    public String getContactGender() {
        return ContactGender;
    }

    public void setContactGender(String contactGender) {
        ContactGender = contactGender;
    }

    public String getContactNote() {
        return ContactNote;
    }

    public void setContactNote(String contactNote) {
        ContactNote = contactNote;
    }
}
